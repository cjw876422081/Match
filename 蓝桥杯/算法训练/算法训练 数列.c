/*
问题描述
　　给定一个正整数k(3≤k≤15),把所有k的方幂及所有有限个互不相等的k的方幂之和构成一个递增的序列，
	例如，当k=3时，这个序列是：
　　1，3，4，9，10，12，13，…
　　（该序列实际上就是：30，31，30+31，32，30+32，31+32，30+31+32，…）
　　请你求出这个序列的第N项的值（用10进制数表示）。
　　例如，对于k=3，N=100，正确答案应该是981。
输入格式
　　只有1行，为2个正整数，用一个空格隔开：
　　k N
　　（k、N的含义与上述的问题描述一致，且3≤k≤15，10≤N≤1000）。
输出格式
　　计算结果，是一个正整数（在所有的测试数据中，结果均不超过2.1*109）。（整数前不要有空格和其他符号）。
样例输入
3 100
样例输出
981
*/
#include<stdio.h>
int jisuan( int , int );
int main(Void)
{
	int k ,  n ; 
	scanf("%d%d", & k , & n );
	printf("%d\n", jisuan( k , n ));
	return 0 ; 
}
int jisuan( int k, int n)
{
	int i = 0 , j , now = 1 ;
	int sz[1000]={0} ;
	while( i < n ) 
	{
		int tmp = i ; 
		sz[i ++ ] = now ;
		for( j = 0 ; j < tmp && i < n ; j ++ )
		{
			sz[i ++] = now + sz[j] ; 
		}
		now *= k ;
	}
	return sz[n-1] ;
}
