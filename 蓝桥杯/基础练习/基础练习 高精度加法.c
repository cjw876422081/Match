/*
问题描述
　　输入两个整数a和b，输出这两个整数的和。a和b都不超过100位。
算法描述
　　由于a和b都比较大，所以不能直接使用语言中的标准数据类型来存储。对于这种问题，一般使用数组来处理。
　　定义一个数组A，A[0]用于存储a的个位，A[1]用于存储a的十位，依此类推。同样可以用一个数组B来存储b。
　　计算c = a + b的时候，首先将A[0]与B[0]相加，如果有进位产生，则把进位（即和的十位数）存入r，
把和的个位数存入C[0]，即C[0]等于(A[0]+B[0])%10。然后计算A[1]与B[1]相加，
这时还应将低位进上来的值r也加起来，即C[1]应该是A[1]、B[1]和r三个数的和．
如果又有进位产生，则仍可将新的进位存入到r中，和的个位存到C[1]中。依此类推，即可求出C的所有位。
　　最后将C输出即可。
输入格式
　　输入包括两行，第一行为一个非负整数位a，第二行为一个非负整数b。两个整数都不超过100位，两数的最高都不是0。
输出格式
　　输出一行，表示a + b的值。
样例输入
20100122201001221234567890
2010012220100122
样例输出
20100122203011233454668012
*/
#include<stdio.h>
void shuru( int [] , int *);
void q_nx( int [] , int );
void shuchu( int [] , int);
void q_he(int [] , int , int [] , int , int [] , int * );
void put_in(int [] , int [] , int [] , int  );
int main( void )
{
	int s1[100]={0} ,s2[100]={0} ,c[101]={0};
	int cd1=0,cd2=0;
	shuru(s1 , &cd1 );
	shuru(s2 , &cd2 );
	int ws=0;
	q_he(s1 , cd1 , s2 , cd2 , c , &ws);
	shuchu( c , ws);
	return 0;
}
void put_in(int s1[] , int s2[] , int c[] , int cd )
{
	int i;
	for(i = 0 ; i < cd ; i ++)
	{
		c[i]=s1[i]+s2[i];
	}
}
void q_he(int s1[] , int cd1, int s2[] , int cd2, int c[] , int * ws)
{
	int n=cd1 > cd2 ? cd1 : cd2;
	put_in( s1 , s2 , c , n);
	int i;
	for(i = 0 ; i < n ; i ++)
	{
		c[i+1] += c[i]/10;
		c[i] %= 10;
		++ *ws;
	}
	if(c[n] > 0)
	{
		++ *ws;
	}
	
}
void shuchu( int sz[] , int n)
{
	int i;
	for( i = n-1 ; i >=0 ; i--)
	{
		printf("%d",sz[i]);
	}
	putchar('\n');
}
void q_nx( int sz[] , int n)
{
	int tou=0,wei = n -1;
	while(tou < wei)
	{
		int tmp=sz[tou];
		sz[tou]=sz[wei];
		sz[wei]=tmp;
		tou++;
		wei--;
	}
}
void shuru( int sz[] , int * cd)
{
	char c;
	while((c = getchar()) !='\n' )
	{
		sz[*cd] = c -'0';
		++ *cd ;
	}
	q_nx( sz , * cd );
}

